from tkinter import *

# Objekt window der Klasse Tk erstellen
window = Tk()

# Aufruf der Methode geometry mit Übergabe eines Strings der die Größe enthält
window.geometry("600x300")

# Aufruf der Methode title mit Übergabe eines Strings der den Titel enthält
window.title("Promille-Rechner")

# Dialog darstellen
window.mainloop()