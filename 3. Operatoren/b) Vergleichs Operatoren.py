# Gleich
a = 5
b = 5
isEqual = a == b
print (a, "und", b, "sind gleich:", isEqual)

# Ungleich
a = "5"
b = 5
isUnequal = a != b
print (a, "und", b, "sind ungleich:", isUnequal)

# Größer & kleiner
print("Größer:", 10 > 5)
print("Kleiner:", 5 < 10)
print("Größer gleich:", 5 >= 5)
print("Kleiner gleich:", 5 <= 5)
