# WAS SIND KLASSEN UND OBJEKTE?

# - Mit Klassen bündeln bzw. separieren wir Code und erstellen einen Bauplan für einen
# bestimmten Sachverhalt

# - Objekte sind Instanzen von Klassen, also eine konkret erstellte Instanz einer Klasse,
# mit der wir in unserem Code arbeiten können

# - Beispiel: Wir werden eine Klasse mit dem Namen Person erstellen, die drei allgemeine
# Eigenschaften einer Person beinhaltet. Zu dieser Klasse werden wir mehrere Objekte erstellen,
# also Instanzen der Klasse Person, mit denen wir in unserem Code arbeiten möchten.
