# float
first_variable = 0.0
print(type(first_variable))

second_variable = 1.1
print(type(second_variable))

# string
first_variable = "Januar"
print(type(first_variable))

second_variable = "Februar"
print(type(second_variable))

# boolean
first_variable = True
print(type(first_variable))

second_variable = False
print(type(second_variable))


